# Ввод количества учащихся в трех классах
class1 = int(input("Введите количество учащихся в первом классе: "))
class2 = int(input("Введите количество учащихся во втором классе: "))
class3 = int(input("Введите количество учащихся в третьем классе: "))
# Вычисление количества парт для каждого класса (с округлением вверх)
desks1 = (class1 + 1) // 2  
desks2 = (class2 + 1) // 2
desks3 = (class3 + 1) // 2
# Вычисление общего количества парт
total_desks = desks1 + desks2 + desks3
# Вывод результата
print(total_desks)
